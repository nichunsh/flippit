$(document).ready(function(){
	console.log("hi")

	$('#arrow').animate({
		right: '0%'
	}, 800, false)

	$('.up').each(function(i){
		$(this).delay(i*100).animate({
			bottom: '0px'
		}, 800, "easeOutBounce")
	});

	displayNav()
	$('#arrow').on("click", function(){

		if($(window).width() < 768) {

			if($('#nav').is(':hidden')) {
				$('.navbutton').each(function(i){
					$(this).fadeIn(800, "easeOutQuad")
				});
				$('#nav').slideDown(800, "easeOutQuad")
				$('#arrow').addClass('rotate')
				
			} else {
				$('.navbutton').each(function(i){
					$(this).fadeOut(800, "easeInQuad")
				});
				$('#arrow').removeClass('rotate')
				$('#nav').slideUp(800, "easeInQuad")
				
			}

		}
	});


	$(window).on('resize', function(){
		displayNav()
	});

});


function displayNav() {
	if($(window).width() < 768) {
		if ($('#arrow').hasClass('rotate')){
			$('#nav').show()
			$('.navbutton').show()
		}else {
			$('.navbutton').hide()
			$('#nav').hide()
		}
	} else {
		$('#nav').show()
		$('.navbutton').show()
		if ($('#arrow').hasClass('rotate')) {
			$('#arrow').removeClass('rotate')
		}
	}
}




